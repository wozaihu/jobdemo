package com.example.jobdemo.bean;

public class PictureBean {

    /**
     * code : 200
     * imgurl : https://cdn.jsdelivr.net/gh/ssrss/img/4128.jpg
     * width : 1920
     * height : 1200
     */

    private int code;
    private String imgurl;
    private int width;
    private int height;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getImgurl() {
        return imgurl;
    }

    public void setImgurl(String imgurl) {
        this.imgurl = imgurl;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
